				<td class="pqr">
					<p align="center">
						The full range of operations in which a nation's naval forces may be involved is vast, ranging from high intensity war 
						fighting at one end to humanitarian assistance and disaster relief operations at the other end. This broad continuum of 
						operations can be broken down into distinct roles, each demanding a specific approach to the conduct of operations.
						
						Accordingly, the four main roles envisaged for the IN are as follows :-<br>
						<img src="../images/35.jpg" align="center" title="The Military Role"><br>
						The essence of all navies is their military character. In fact, the Raison d’etre of navies is to ensure that no 
						hostile maritime power can degrade own national security and interests. The navy's military role is characterized 
						by the threat or use of force at and from the sea. This includes application of maritime power in both offensive 
						operations against enemy forces territory and trade, and defensive operations to protect own forces, territory 
						and trade. The military role is performed through the accomplishment of specific military objectives, missions 
						and tasks.<br>
						<img src="../images/36.jpg" align="center" title="The Diplomatic Role"><br><br>
						Naval Diplomacy entails the use of naval forces in support of foreign policy objectives to build ‘bridges of 
						friendship’ and strengthen international cooperation on the one hand, and to signal capability and intent to 
						deter potential adversaries on the other. The larger purpose of the navy’s diplomatic role is to favorably shape 
						the maritime environment in the furtherance of national interests, in consonance with the foreign policy and 
						national security objectives. Navies inherently lean towards performing a diplomatic role on account of two 
						characteristics. The first is their status as comprehensive instruments of a country’s sovereign power, 
						whereupon their very presence in or off a certain area signals the nation’s political intent and commitment to 
						pursue national interests in that region. Hence, their presence or absence can be calibrated to send a political 
						message to potential friends and foes alike.<br>
						<img src="../images/38.jpg" align="center" title="Constabulary Role"><br><br>
						The increasing incidence of maritime crime has brought into sharp focus the constabulary role that navies have 
						to perform. The significance of this role may be Gauged from the fact that for a third of the world’s navies, this is 
						a major facet of their functions. In the constabulary role, forces are employed to enforce law of the land or to 
						implement a regime established by an international mandate. Force is only employed for self-defense or as a 
						last resort in execution of this role. The protection and promotion of India’s maritime security is one of the prime 
						responsibilities of the Indian Navy. This includes a constabulary element, especially where it relates to threats 
						that involve use of force at sea. The range of tasks that the IN has to undertake in the constabulary role ranges 
						from Low Intensity Maritime Operations (LIMO) to maintaining good order at sea. This further includes aspects of 
						coastal security, as part of India’s overall maritime security. Constabulary tasks at sea are neither the primary 
						nor the sole mandate of the IN. With the establishment of the ICG in February 1978, law enforcement aspects of 
						the constabulary role within the Maritime Zones of India (MZI) have been transferred to the ICG.<br>
						<img src="../images/47.jpg" align="center" title="Begin Role"><br><br>
						The ‘benign’ role is so named because violence has no part to play in its execution, nor is the potential to apply 
						force a necessary prerequisite for undertaking these operations. Examples of benign tasks include humanitarian 
						aid, disaster relief, Search and Rescue (SAR), ordnance disposal, diving assistance, salvage operations, 
						Hydrographic surveys, etc. Maritime forces, because of their quick mobilization, are extremely useful in the early 
						stages of a crisis for providing relief material, first aid and succour. Much of the capacity to perform these 
						functions derives itself from the mobility, reach and endurance inherent in naval task forces, coupled with their 
						SeaLift capability. For example, in the immediate aftermath of a natural disaster, one of the biggest challenges 
						is the disbursement of food, water and relief material. It is under such conditions that military mobility, coupled 
						with reliable communications are most effective in ensuring distribution to even the most remote afflicted areas.<br>
