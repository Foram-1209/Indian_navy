					<p align="center">
						<img src="../images/27.jpg" align="center"><br>
						INS Shivalik Entering Fremantle Harbour<br><br>
						In pursuance of India’s ‘Act East’ policy, Eastern Fleet under the command of Rear Admiral Biswajit Dasgupta, YSM, VSM, 
						the Flag Officer Commanding Eastern Fleet are on an overseas deployment to the South East Asia and Southern Indian 
						Ocean. Indian Naval Ships Jyoti, Shivalik and Kamorta are on a port visit to Freemantle, Australia from 13 June to 17 June 
						2017. Vice Admiral HCS Bisht, PVSM, AVSM, ADC, Flag Officer Commanding Eastern Naval Command is also visiting 
						Freemantle during the period.<br>
						<img src="../images/28.jpg" align="center"><br>
						RAdm Biswajit Dasgupta, FOCEF speaking during the welcome ceremony of AUSINDEX-17 bilateral maritime 
						exercise with Australian Navy<br><br>
						The visit of the Indian Naval Ships seeks to underscore India’s peaceful presence and solidarity with friendly and 
						harmonious countries towards ensuring good order in the maritime domain and to strengthen existing bonds between 
						India and Australia. The two countries share a rich history and a strategic relationship. A number of bilateral agreements 
						exist between the two countries in a various spheres.<br>
						<img src="../images/29.jpg" align="center"><br>
						RAdm Biswajit Dasgupta FOCEF being recieved by Cmde Brett dowsing the Sr officer Western Australia 
						SOWA Capt Brian delmont the Sr Naval Offr Western Australia SNOWA<br><br>
						During the stay in harbour, various activities such as official calls, formal reception on board ships, ships open to visitors, 
						guided tours for Indian naval personnel and professional interaction between naval personnel of both the nations have 
						been planned. AUSINDEX-17, a bilateral maritime exercise between the two navies would also be held as part of the visit. 
						This would be the second edition of the exercise with the maiden edition having been conducted at Visakhapatnam in 
						2015. The exercises aim to increase interoperability and are in consonance with the growing cooperation between the 
						two countries. In April this year, Prime Minister, Mr Malcom TurnbuII visited India and called on the Indian Prime Minister, 
						Shri Narendra Modi for bilateral talks and enhance the partnership between the two countries.<br>
						<img src="../images/30.jpg" align="center"><br>
						Cmde Brett Dowsing the Sr Officer Western Australia SOWA Capt Brian Delmont the Sr Naval Offr Western 
						Australia SNOWA calling-on RAdm B Dasgupta<br><br>
						Indian Naval assets have been increasingly deployed in recent times to address the main maritime concerns of the 
						region. In addition, as part of the Indian Government’s vision of SAGAR (Security and Growth for All in the Region), the 
						Indian Navy has also been involved in assisting countries in the Indian Ocean Region with EEZ Surveillance, Search and 
						Rescue, and other capacity-building and capability-enhancement activities. The current deployment will contribute 
						towards the Indian Navy’s efforts to consolidate inter-operability and forge strong bonds of friendship across the 
						Seas.<br><br>
						Over the past few decades, India has made substantial advances in terms of designing and building warships 
						indigenously and the visiting ships bear testimony to these capabilities. INS Shivalik, a muIti-role stealth frigate is 
						commanded by Captain R Vinod Kumar while INS Kamorta, an Anti-Submarine Warfare Corvette is commanded by 
						Commander Vipin Gupta and INS Jyoti, the fleet replenishment tanker is commanded by Captain S Shyam Sundar.<br>
