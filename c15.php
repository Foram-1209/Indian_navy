						Welcome by COS 3rd NAC R Adm Montree Jung Mankung <br>
						Indian Naval Ships Tir, Sujata, Sail Training Ship (STS) Sudarshini alongwith Indian Coast Guard Ship Varuna, comprising 
						the 1st Training Squadron, are scheduled to visit Phuket, Thailand from 04 – 08 Apr 16 as part of their Overseas 
						Deployment during Spring Term 16. The Senior Officer of the 1st Training Squadron, Captain SR Ayyar, would be 
						embarked on board INS Tir.<br>
						<img src="../images/121.jpg" align="center"><br>
						CSR Activity at Bangla Street,  Patong Beach<br><br>
						The First Training Squadron forms part of Southern Naval Command (SNC) and comprises Indian Naval Ships Tir, 
						Shardul, Sujata, ICGS Varuna and two Sail Training Ships Sudarshini and Tarangini, all of which have been built in India. 
						The primary aim of the Squadron is to impart sea training to Naval and Coast Guard trainees, with a 24 weeks ab-initio 
						sea training being imparted. All the trainees are trained in Seamanship, Navigation, Ship Handling, Boat Work, Technical 
						aspects, etc. whilst being exposed to the rigours of life at sea, so as to earn their ‘sea legs’.<br>
						<img src="../images/122.jpg" align="center"><br>
						Manoeuvres between Indian Navy and RTN units<br><br>
						The Southern Naval Command (SNC) is the Training Command of the Indian Navy, which provides both basic and 
						advanced training to officers and sailors of the Indian Navy. Vice Admiral Girish Luthra, AVSM, VSM is the Flag Officer 
						Commanding-in-Chief, Southern Naval Command. The Indian Navy has also been providing training to personnel from 
						friendly foreign countries for more than four decades, wherein more than 13,000 personnel from over 40 countries have 
						been trained. The Indian Navy’s focused approach for providing high quality training by constantly adapting to evolving 
						tactics and technologies, has gained it a reputation of being one of the finest training destinations.<br>
						<img src="../images/123.jpg" align="center"><br>
						Naval Band Concert at Jung Ceylon Mall-Patong<br><br>
						India and Thailand have a close, long-standing relationship covering a wide spectrum of activities and interactions, which 
						have strengthened over the years. The present deployment of the Training Squadron to Phuket provides opportunities
						for extensive maritime engagement, contributes to the maintenance of good order at sea and further cements the close 
						relations between the two nations and the two navies.<br>
