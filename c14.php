					<p align="center">
						<img src="../images/117.jpg" align="center"><br>
						Flag host and Semaphore Drills<br><br>
						INS Karmuk, an indigenously built Missile Corvette based at the Andaman and Nicobar Command, alongwith a Dornier 
						Maritime Patrol Aircraft, are participating in the 22nd Indo-Thai Coordinated Patrol (CORPAT), scheduled from 19-27 Apr 
						16 in the Andaman Sea.<br>
						<img src="../images/118.jpg" align="center"><br><br>
						Karmuk VBSS team onboard KLAENG<br><br>
						Maritime interaction between India and Thailand has been growing steadily with frequent port visits, participation in 
						multilateral exercises and training exchanges. Under the broad ambit of this strong maritime relationship, the two navies 
						have been carrying out CORPATs along the International Maritime Boundary Line (IMBL) twice a year since 2005, with 
						the aim of keeping this vital part of the Indian Ocean Region safe and secure for commercial shipping and international 
						trade. The CORPAT has also strengthened understanding and interoperability between the navies and facilitated 
						institution of measures to prevent unlawful activities at sea as well as conduct Search and Rescue (SAR) operations.<br>
						<img src="../images/119.jpg" align="center"><br><br>
						RTN Dornier - The Participating Forces<br><br>
						The 22nd edition of the CORPAT would include participation of one warship and one Maritime Patrol Aircraft from both 
						sides with the Closing Ceremony being held at Port Blair, Headquarters Andaman and Nicobar Command (HQANC) from 
						25-27 Apr 16. The Flag Officer Commanding-in-Chief, Andaman and Nicobar Command, Vice Admiral BK Verma, AVSM 
						and Rear Admiral Warongkorn Osathanonda, Commander, Phangnga Naval Base, 3rd Naval Area Command, of the 
						Royal Thailand Navy will be the Senior Officers at the Closing Ceremony.<br><br>
						India and Thailand have traditionally enjoyed a close and friendly relationship covering a wide spectrum of activities and 
						interactions, which have strengthened over the years. The 22nd edition of CORPAT would bolster the already strong 
						bilateral relationship between the two nations and contribute significantly to enhancing maritime security in the region.<br>
