					<p align="center">
						Royal Australia Navy (RAN) ship, HMAS Newcastle commanded by Commander MD Sirois is visiting Kochi from 04 - 07 
						Jul 17. On 04 Jul 17, the Commanding Officer of the visiting RAN Ship accompanied by Ms. Harinder Sidhu, High 
						Commissioner of Australia and Captain Sheldon Williams, Defence Attache called on Vice Admiral AR Karve AVSM, Flag 
						Officer Commanding- in- Chief Southern Naval Command (SNC) for interactions on issues of common interest to both 
						navies. A friendly volleyball match between teams from SNC and the visiting ship was also played.<br>
						<img src="../images/19.jpg" align="center"><br>
						Commanding officer of  HMAS Newcastle calling on Vice Admiral AR Karve<br><br>
						During the ship’s stay in harbour, various activities such as guided tour for Indian Naval personnel onboard HMAS 
						Newcastle, conducted tour of some of the training units under SNC at Kochi like the Navigation & Direction, Anti- 
						Submarine Warfare School, Damage Control Training Facility ‘Avinash,’ for the visiting ship’s crew have been planned.  
						The crew from the HMAS Newcastle would be also visiting places of interest at Fort Kochi, Marine Drive and various 
						museums at Kochi. On their departure on 07 Jul 17, the RAN ship is planned to participate in a passage Exercise with IN 
						Ships off Kochi.<br>
						<img src="../images/20.jpg" align="center"><br>
						HMAS Newcastle<br><br>
						Recently, IN Ships Jyoti, Shivalik and Kamorta from Eastern Fleet had also visited Freemantle, Australia from 13–17 June 
						and participated in the second edition of AUSINDEX- 17, a bilateral maritime exercise between the two navies. The naval 
						cooperation between the two countries is in pursuance of India’s Act East policy to promote bilateral relations and 
						enhance interoperability between both navies.<br>
