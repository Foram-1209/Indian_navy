				<td class="pqr">
					<p align="center"><font face="impact" size="4">
						Naval co-operation between India, US and Japan epitomises the strong and resilient relationship between the three 
						democracies. The MALABAR series of exercises, initiated in 1992 between the Indian and US Navies, have steadily 
						grown in scope, complexity and participation into a multifaceted exercise with the participation of Japanese Maritime 
						Self Defense Force (JMSDF).
						<img src="../images/16.jpg" align="center"><br><br>
						The 21st edition of the exercise, MALABAR-17 will be conducted in the Bay of Bengal from 10 to 17 July 2017. The primary 
						aim of this exercise is to increase interoperability amongst the three navies as well as develop common understanding 
						and procedures for maritime security operations. The scope of MALABAR-17 includes wide-ranging professional 
						interactions during the Harbour Phase at Chennai from 10 to 13 July 2017 and a diverse range of operational activities at 
						sea during the Sea Phase from 14 to 17 July 2017. The thrust of exercises at sea this year would be on Aircraft Carrier 
						operations, Air Defence, Anti-Submarine Warfare (ASW), Surface Warfare, Visit Board Search and Seizure (VBSS), Search 
						and Rescue, Joint Manoeuvres and Tactical procedures. In addition, officials from the three countries will be flown 
						onboard the ships at sea on 15 July 2017.
						<img src="../images/17.jpg" align="center"><br><br>
						MALABAR-17 will be another milestone with participation of 16 ships, two submarines and more than 95 aircraft, towards 
						strengthening mutual confidence and inter-operability as well as sharing of best practices between the Indian, Japanese 
						and US Navies. The exercise is a demonstration of the joint commitment of all three nations to address common 
						maritime challenges across the spectrum of operations and will go a long way in enhancing maritime security in the Indo-
						Pacific region, for the benefit of the global maritime community.<br><br>
