				<td class="pqr">
					<p align="center">
						The first indigenously built Floating Dock (FDN-2) for the Indian Navy was launched on 20 June 2017 by Smt Anjali 
						Deshpande, wife of Vice Admiral DM Deshpande, AVSM, VSM, Controller of Warship Production & Acquisition at an 
						impressive ceremony at Larsen & Toubro Ltd (L&T) Shipyard at Kattupalli near Chennai.<br><br>
						<img src="../images/23.jpg" align="center"><br>
						After formal welcome by Vice Admiral B Kannan (Retd), PVSM, AVSM, VSM, Head of Ship Building, L&T the ceremony 
						commenced with an address by the Chief Guest, Vice Admiral DM Deshpande, AVSM, VSM, Controller of Warship 
						Production & Acquisition. Thereafter in accordance with the traditions, Smt. Anjali Deshpande applied ‘Kumkum’ on the 
						Floating Dock. She wished the dock good luck and launched the vessel.<br><br>
						<img src="../images/24.jpg" align="center"><br>
						Speaking on the occasion, Vice Admiral DM Deshpande, commended L&T for their efforts in the design and construction 
						of FDN-2. He brought out that launching of the indigenously built Floating Dock, bears testimony to the capabilities 
						available in India for realising the ‘Make in India’ vision. He extended warm felicitations to the entire team of the L&T for 
						the achievement.<br><br>
						<img src="../images/25.jpg" align="center"><br>
						Floating Dock is an indigenously designed and built platform with state-of-the-art machinery & control systems capable 
						of docking warships of up to 8000 Tons displacement.  It has high capacity Ballast Pumps, along with advanced 
						automated Ballast Control System. The dock covers provided with the FDN-2 facilitate repair & refit activities in 
						inclement weather conditions.<br><br>
						<img src="../images/26.jpg" align="center"><br>
						The launch of Floating Dock (FDN-2), Yard 55000, designed in-house and built by L&T Shipyard, Kattupalli is a milestone 
						in India’s quest for self-reliance in shipbuilding.<br>
